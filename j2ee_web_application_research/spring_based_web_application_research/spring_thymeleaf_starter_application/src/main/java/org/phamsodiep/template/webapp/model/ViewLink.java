package org.phamsodiep.template.webapp.model;


public class ViewLink {
  private String path;
  private String name;


  public String getPath() {
    return this.path;
  }

  public String getName() {
    return this.name;
  }

  public void setPath(String path) {
    this.path = path;
  }

  public void setName(String name) {
    this.name = name;
  }
}


